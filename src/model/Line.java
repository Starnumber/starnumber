package model;

public class Line {
	private int line_id;
	private String line_name;
	private int user_id;
	private int line_type_number;
	public int getLine_id() {
		return line_id;
	}
	public void setLine_id(int line_id) {
		this.line_id = line_id;
	}
	public String getLine_name() {
		return line_name;
	}
	public void setLine_name(String line_name) {
		this.line_name = line_name;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getLine_type_number() {
		return line_type_number;
	}
	public void setLine_type_number(int line_type_number) {
		this.line_type_number = line_type_number;
	}

	
	
}
